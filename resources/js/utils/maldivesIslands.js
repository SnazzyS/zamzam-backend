// Get islands formatted for dropdown (name with atoll)
export const getIslandOptions = () => {
    return maldivesIslands
        .map(island => ({
            value: island.name,
            label: `${island.name} (${island.atoll})`,
            atoll: island.atoll,
        }))
        .sort((a, b) => a.label.localeCompare(b.label));
};

// Search islands by name or atoll
export const searchIslands = (query) => {
    if (!query) return getIslandOptions();
    const lowerQuery = query.toLowerCase();
    return getIslandOptions().filter(
        island =>
            island.value.toLowerCase().includes(lowerQuery) ||
            island.atoll.toLowerCase().includes(lowerQuery)
    );
};
