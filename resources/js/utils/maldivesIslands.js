// Get islands formatted for dropdown (Dhivehi names with atoll)
export const getIslandOptions = () => {
    return maldivesIslands
        .map(island => ({
            value: island.name,
            label: `${island.nameDv} (${island.atollDv})`,
            labelEn: `${island.name} (${island.atoll})`,
            nameDv: island.nameDv,
            atoll: island.atoll,
            atollDv: island.atollDv,
        }))
        .sort((a, b) => a.nameDv.localeCompare(b.nameDv, 'dv'));
};

// Search islands by name (Dhivehi or English) or atoll
export const searchIslands = (query) => {
    if (!query) return getIslandOptions();
    const lowerQuery = query.toLowerCase();
    return getIslandOptions().filter(
        island =>
            island.value.toLowerCase().includes(lowerQuery) ||
            island.nameDv.includes(query) ||
            island.atoll.toLowerCase().includes(lowerQuery) ||
            island.atollDv.includes(query)
    );
};
